CREATE TABLE ADMINISTRADOR (
    ID VARCHAR (32) NOT NULL,
    SW VARCHAR (32) NOT NULL,
    PRIMARY KEY CLUSTERED (ID ASC)
);

CREATE TABLE CARONA (
    RG     VARCHAR (10) NOT NULL,
    PESO   INT          NULL,
    ALTURA INT          NULL,
    PRIMARY KEY CLUSTERED (RG ASC),
    FOREIGN KEY (RG) REFERENCES USUARIO (RG)
);

CREATE TABLE CARONEIRO (
    RG           VARCHAR (10) NOT NULL,
    TIPO_VEICULO VARCHAR (32) NULL,
    PLACA        VARCHAR (8)  NOT NULL,
    MAX_CARONAS  INT          NULL,
    PRIMARY KEY CLUSTERED (RG ASC),
    FOREIGN KEY (RG) REFERENCES USUARIO (RG)
);

CREATE TABLE COMENTARIO (
    RG           VARCHAR (10)  NOT NULL,
    N_COMENTARIO INT           NOT NULL,
    COMENTARIO   VARCHAR (256) NOT NULL,
    DATA         DATE          NOT NULL,
    PRIMARY KEY CLUSTERED (N_COMENTARIO ASC),
    FOREIGN KEY (RG) REFERENCES USUARIO (RG)
);

CREATE TABLE REGIAO (
    ID         INT          NOT NULL,
    BAIRRO     VARCHAR (32) NOT NULL,
    REFERENCIA VARCHAR (64) NULL,
    PRIMARY KEY CLUSTERED (ID ASC)
);

CREATE TABLE USUARIO (
    RG         VARCHAR (10) NOT NULL,
    NOME       VARCHAR (32) NOT NULL,
    DATA_NASC  DATE         NULL,
    HORA_IDA   TIME (7)     NULL,
    HORA_VOLTA TIME (7)     NULL,
    CONTATO    VARCHAR (18) NOT NULL,
    REGIAO     INT          NOT NULL,
    PRIMARY KEY CLUSTERED (RG ASC),
    FOREIGN KEY (REGIAO) REFERENCES REGIAO (ID)
);

